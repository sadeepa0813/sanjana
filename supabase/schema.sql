-- 0. Extensions & Setup
create extension if not exists "uuid-ossp";

-- 1. Profiles Table (Extends Supabase Auth)
create table if not exists public.profiles (
  id uuid references auth.users on delete cascade not null primary key,
  email text unique,
  full_name text,
  role text default 'user' check (role in ('user', 'admin')),
  phone text,
  address text,
  avatar_url text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 2. Products Table
create table if not exists public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  category text not null,
  price decimal(10,2) not null check (price >= 0),
  original_price decimal(10,2) check (original_price >= price),
  stock integer default 0 check (stock >= 0),
  image_url text,
  is_featured boolean default false,
  discount_percent integer default 0 check (discount_percent >= 0 and discount_percent <= 100),
  specifications jsonb default '{}'::jsonb,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 3. Orders Table
create table if not exists public.orders (
  id uuid default uuid_generate_v4() primary key,
  user_id uuid references public.profiles(id) on delete set null,
  order_number text unique not null,
  total_amount decimal(10,2) not null check (total_amount >= 0),
  status text default 'pending' check (status in ('pending', 'confirmed', 'processing', 'shipped', 'delivered', 'cancelled')),
  whatsapp_number text,
  shipping_address text,
  tracking_number text,
  payment_method text default 'cod',
  notes text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 4. Order Items Table (Normalized)
create table if not exists public.order_items (
  id bigint generated by default as identity primary key,
  order_id uuid references public.orders(id) on delete cascade not null,
  product_id bigint references public.products(id) on delete set null,
  quantity integer default 1 check (quantity > 0),
  price_at_time decimal(10,2) not null check (price_at_time >= 0),
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 5. Cart Items Table (Persistent Cart)
create table if not exists public.cart_items (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  product_id bigint references public.products(id) on delete cascade not null,
  quantity integer default 1 check (quantity > 0),
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(user_id, product_id)
);

-- 6. Wishlists Table
create table if not exists public.wishlists (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  product_id bigint references public.products(id) on delete cascade not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(user_id, product_id)
);

-- 7. Reviews Table
create table if not exists public.reviews (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  product_id bigint references public.products(id) on delete cascade not null,
  rating integer not null check (rating >= 1 and rating <= 5),
  comment text,
  is_verified_purchase boolean default false,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(user_id, product_id)
);

-- 8. Discount Codes Table
create table if not exists public.discounts (
  id bigint generated by default as identity primary key,
  code text unique not null,
  description text,
  discount_percent integer not null check (discount_percent > 0 and discount_percent <= 100),
  max_uses integer check (max_uses > 0),
  used_count integer default 0 check (used_count >= 0),
  min_order_value decimal(10,2) default 0 check (min_order_value >= 0),
  valid_from timestamp with time zone default now(),
  valid_until timestamp with time zone,
  is_active boolean default true,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 9. Notifications Table
create table if not exists public.notifications (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  title text not null,
  message text,
  type text default 'system' check (type in ('system', 'order', 'promotion', 'alert')),
  is_read boolean default false,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 10. Feedback/Contact Table
create table if not exists public.feedback (
  id bigint generated by default as identity primary key,
  name text not null,
  email text not null,
  subject text,
  message text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

