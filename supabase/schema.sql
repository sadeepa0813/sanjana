-- Security Helper Functions
create or replace function public.is_admin()
returns boolean as $$
begin
  return exists (
    select 1 from public.profiles
    where id = auth.uid() and role = 'admin'
  );
end;
$$ language plpgsql security definer;

-- Profiles Policies
create policy "Profiles are viewable by everyone" on public.profiles for select using (true);
create policy "Users can update own profile" on public.profiles for update using (auth.uid() = id);

-- Products Policies
create policy "Products are viewable by everyone" on public.products for select using (true);
create policy "Admins can manage products" on public.products for all using (public.is_admin());

-- Orders Policies
create policy "Users can view own orders" on public.orders for select using (auth.uid() = user_id);
create policy "Users can create orders" on public.orders for insert with check (auth.uid() = user_id);
create policy "Admins can view and update all orders" on public.orders for all using (public.is_admin());

-- Order Items Policies
create policy "Users can view own order items" on public.order_items for select using (
  exists (select 1 from public.orders where id = public.order_items.order_id and user_id = auth.uid())
);
create policy "Users can create order items" on public.order_items for insert with check (
  exists (select 1 from public.orders where id = public.order_items.order_id and user_id = auth.uid())
);
create policy "Admins can manage all order items" on public.order_items for all using (public.is_admin());

-- Cart Items Policies
create policy "Users can manage own cart" on public.cart_items for all using (auth.uid() = user_id);

-- Wishlists Policies
create policy "Users can manage own wishlist" on public.wishlists for all using (auth.uid() = user_id);

-- Reviews Policies
create policy "Reviews are viewable by everyone" on public.reviews for select using (true);
create policy "Users can create and update own reviews" on public.reviews for all using (auth.uid() = user_id);
create policy "Admins can manage all reviews" on public.reviews for all using (public.is_admin());

-- Discounts Policies
create policy "Discounts are viewable by everyone" on public.discounts for select using (is_active = true);
create policy "Admins can manage discounts" on public.discounts for all using (public.is_admin());

-- Notifications Policies
create policy "Users can view own notifications" on public.notifications for select using (auth.uid() = user_id);
create policy "Users can update own notifications" on public.notifications for update using (auth.uid() = user_id);
create policy "Admins can manage all notifications" on public.notifications for all using (public.is_admin());

-- Feedback Policies
create policy "Anyone can submit feedback" on public.feedback for insert with check (true);
create policy "Admins can view feedback" on public.feedback for select using (public.is_admin());

